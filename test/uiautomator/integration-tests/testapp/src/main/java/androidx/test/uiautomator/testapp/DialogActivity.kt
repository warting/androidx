package androidx.test.uiautomator.testapp

import android.app.AlertDialog
import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.compose.foundation.layout.Arrangement
import androidx.compose.foundation.layout.Column
import androidx.compose.foundation.layout.fillMaxSize
import androidx.compose.material.Button
import androidx.compose.material.MaterialTheme
import androidx.compose.material.Surface
import androidx.compose.material.Text
import androidx.compose.runtime.Composable
import androidx.compose.runtime.getValue
import androidx.compose.runtime.mutableStateOf
import androidx.compose.runtime.remember
import androidx.compose.runtime.setValue
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.platform.LocalContext

class DialogActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MaterialTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colors.background,
                ) {
                    DialogScreen()
                }
            }
        }
    }
}

@Composable
fun DialogScreen() {
    val context = LocalContext.current
    var dialogResult by remember { mutableStateOf("") }

    Column(
        modifier = Modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally,
    ) {
        Text(text = "Dialog Result: $dialogResult")
        Button(
            onClick = {
                AlertDialog.Builder(context)
                    .setTitle("Confirmation")
                    .setMessage("Press a button")
                    .setPositiveButton("Yes") { _, _ -> dialogResult = "Pressed Yes" }
                    .setNegativeButton("No") { _, _ -> dialogResult = "Pressed No" }
                    .setNeutralButton("Maybe") { _, _ -> dialogResult = "Pressed Maybe" }
                    .show()
            }
        ) {
            Text("Show Dialog")
        }
    }
}
