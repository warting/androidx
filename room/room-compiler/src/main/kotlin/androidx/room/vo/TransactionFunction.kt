package androidx.room.vo

import androidx.room.compiler.processing.XMethodElement
import androidx.room.compiler.processing.XType
import androidx.room.solver.transaction.binder.TransactionFunctionBinder

class TransactionFunction(
    val element: XMethodElement,
    val returnType: XType,
    val parameterNames: List<String>,
    val callType: CallType,
    val functionBinder: TransactionFunctionBinder,
) {
    enum class CallType {
        /** Directly call the function, it has a super implementation */
        CONCRETE,

        /**
         * It has a default implementation and the default implementation is in the DAO interface
         */
        DEFAULT_JAVA8,

        /** Has DefaultImpl generated by Kotlin */
        DEFAULT_KOTLIN,
    }
}
